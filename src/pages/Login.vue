    <Layout>
     <div class="justify-center mt-12">
    <div class="flex flex-col items-center flex-1 h-full justify-center px-4 sm:px-0">
        <div class="flex rounded-lg shadow-lg w-full sm:w-3/4 lg:w-1/2 bg-white sm:mx-0" style="height: 500px">
            <div class="flex flex-col w-full md:w-1/2 p-4">
                <div class="flex flex-col flex-1 justify-center mb-8">
                    <h1 class="text-4xl text-center font-thin">Welcome Back</h1>
                    <div class="w-full mt-4">
                        <form @submit.prevent="onSubmit" class="form-horizontal w-3/4 mx-auto" method="POST" action="#">
                            <div class="flex flex-col mt-4">
                                <input id="email" v-model="form.firstName" type="text" class="flex-grow h-8 px-2 border rounded border-grey-400" name="email" value="" placeholder="Email">
                            </div>
                            <div class="flex flex-col mt-4">
                                <input id="password"  type="password" class="flex-grow h-8 px-2 rounded border border-grey-400" name="password" required placeholder="Password">
                            </div>
                            <div class="flex items-center mt-4">
                                <input type="checkbox" name="remember" id="remember" class="mr-2"> <label for="remember" class="text-sm text-grey-dark">Remember Me</label>
                            </div>
                            <div class="flex flex-col mt-8">
                                <button type="submit" class="bg-blue-500 hover:bg-blue-700 text-white text-sm font-semibold py-2 px-4 rounded">
                                    Login
                                </button>
                            </div>
                        </form>
                        
                    </div>
                </div>
            </div>
            <div class="hidden md:block md:w-1/2 rounded-r-lg" style="background: url('images/workplace.jpg'); background-size: cover; background-position: center center;"></div>
        </div>
    </div>
</div> 

 </layout>
</template>

<script>
export default {
  metaInfo: {
    title: 'Login'
  },
  data () {
    return {
      form: {
        firstName: '',
        lastName: '',
        email: ''
      }
    }
  },
   computed: {
    formIsValid () {
      return (
        this.form.firstName.length > 0 &&
        this.form.lastName.length > 0 &&
        this.form.email.length > 0
      )
    }
  },
  methods: {
    onSubmit () {
      if (!this.formIsValid) return
      axios
      .post('http://localhost:3000/dolphins', { params: this.form })
      .then ( response => { 
          console.log('form submitted', response)
        }).catch (err => {
          console.log('An error occured', err)
        })
    }
  }
}

</script>

